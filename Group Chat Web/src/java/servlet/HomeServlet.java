    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        // check Log in
        try {
            User u = getUserInformation(request);
            request.setAttribute("user", u);
            request.setAttribute("onlineList", getOnlineUserList());
            request.setAttribute("recentMessageList", getRecentMessage());
            if (!getOnlineUserList().contains(u)) {
                getOnlineUserList().add(u);
            }
            RequestDispatcher rd = this.getServletContext().getRequestDispatcher("/WEB-INF/home.jsp");
            rd.forward(request, response);
            return;
        } catch (MyException e) {
            request.setAttribute("exception", e);
            RequestDispatcher rd = this.getServletContext().getRequestDispatcher("/WEB-INF/exception.jsp");
            rd.forward(request, response);
            return;
        }
    }

